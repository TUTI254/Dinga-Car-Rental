export const getVehicleById = (id: string): Vehicle | undefined =>
  vehicles.find((v) => v.id === id);

export const getFeaturedVehicles = (count = 8): Vehicle[] =>
  vehicles.filter((v) => v.isAvailable).sort((a, b) => b.rating - a.rating).slice(0, count);

export const getVehiclesByCategory = (category: string, count?: number): Vehicle[] => {
  const filtered = vehicles.filter((v) => v.category === category && v.isAvailable);
  return count ? filtered.slice(0, count) : filtered;
};

export const getBudgetVehicles = (maxPrice = 6000, count = 8): Vehicle[] =>
  vehicles.filter((v) => v.pricePerDay <= maxPrice && v.isAvailable)
    .sort((a, b) => a.pricePerDay - b.pricePerDay).slice(0, count);

export const getLuxuryVehicles = (minPrice = 20000, count = 8): Vehicle[] =>
  vehicles.filter((v) => v.pricePerDay >= minPrice && v.isAvailable)
    .sort((a, b) => b.rating - a.rating).slice(0, count);

export const getElectricVehicles = (count = 6): Vehicle[] =>
  vehicles.filter((v) => v.fuelType === "electric" && v.isAvailable).slice(0, count);

export const getSUVVehicles = (count = 8): Vehicle[] =>
  vehicles.filter((v) => v.category === "suv" && v.isAvailable)
    .sort((a, b) => b.rating - a.rating).slice(0, count);

export const getSimilarVehicles = (vehicle: Vehicle, count = 4): Vehicle[] =>
  vehicles.filter((v) => v.id !== vehicle.id && v.category === vehicle.category && v.isAvailable)
    .sort((a, b) => b.rating - a.rating).slice(0, count);

